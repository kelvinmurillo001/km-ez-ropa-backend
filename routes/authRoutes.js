import express from 'express';
import {
  loginAdmin,
  loginCliente,
  refreshToken,
  getUsuarioActual
} from '../controllers/authController.js';

import logger from '../utils/logger.js';
import authMiddleware from '../middleware/authMiddleware.js';
import {
  loginValidation,
  loginClienteValidation
} from '../validators/authValidator.js';

const router = express.Router();

/* â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€ */
/* ðŸ” LOGIN ADMINISTRADOR                        */
/* POST /api/auth/login                          */
/* â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€ */
router.post(
  '/login',
  loginValidation,
  (req, res, next) => {
    logger.info(`ðŸ” POST ${req.originalUrl} - Intento de login admin desde IP: ${req.ip}`);
    return loginAdmin(req, res, next);
  }
);

/* â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€ */
/* ðŸ‘¤ LOGIN CLIENTE                              */
/* POST /api/auth/login-cliente                  */
/* â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€ */
router.post(
  '/login-cliente',
  loginClienteValidation,
  (req, res, next) => {
    logger.info(`ðŸ‘¤ POST ${req.originalUrl} - Intento de login cliente desde IP: ${req.ip}`);
    return loginCliente(req, res, next);
  }
);

/* â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€ */
/* ðŸ” REFRESH TOKEN JWT                          */
/* POST /api/auth/refresh                        */
/* â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€ */
router.post(
  '/refresh',
  (req, res, next) => {
    logger.debug(`ðŸ”„ POST ${req.originalUrl} - Solicitud refresh token desde IP: ${req.ip}`);
    return refreshToken(req, res, next);
  }
);

/* â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€ */
/* ðŸ”Ž DATOS DEL USUARIO AUTENTICADO              */
/* GET /api/auth/me                              */
/* â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€ */
router.get(
  '/me',
  authMiddleware,
  getUsuarioActual
);

export default router;
